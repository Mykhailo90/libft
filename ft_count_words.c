#include "libft.h"

size_t		ft_count_words(char const *s, char c)
{
	size_t	i;

	i = 0;
	while (*s)
	{
		if (*s == c)
			s++;
		while (*s != c)
		{
			if (*(s + 1) == c || *(s + 1) == '\0')
				i++;
			if (*(s + 1) == '\0')
				return (i);
			s++;
		}
		if (*(s + 1) == '\0')
			return (i);
	}
	return (i);
}
